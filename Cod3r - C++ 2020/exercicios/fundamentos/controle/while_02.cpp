#include <iostream>
#include <cstdio>
using namespace std;

int main () {

    int i = 0; //valor inicial - controle laço

    while (i < 10)  // condição
    {
        i++; // incremento
        cout << i << endl; //imprime o valor com incremento
        
    }
    cout << " " << endl;
    int j =  100;    
    while (j >= 0) {
        cout << j << endl;
        j-= 2;
    }

return 0;
}